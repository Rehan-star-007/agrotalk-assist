export function getTranslation<T extends keyof typeof translations>(
  section: T,
  language: string
) {
  const lang = (language as SupportedLanguage) || 'en';
  const sectionData = translations[section];
  return (sectionData[lang as keyof typeof sectionData] || sectionData['en']) as typeof sectionData['en'];
}

